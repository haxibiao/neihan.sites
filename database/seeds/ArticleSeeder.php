那时候，他所在的医院，每年能为 2.6 万人诊治。黄宽一生，帮 3000 多人取出了膀胱结石。

更深远的影响，则是他参与筹建了中国第一所医学院「南华医学堂」。正是在这里，报国无门的他将自己的语言特长一点点融入了对医疗术语的翻译中。他既是解剖学、生理学和外科的教师，还撰写了一套教材，包括 3 年制的基础理论与两年的临床实践，并为学校建立了实验室与标准间。

