1846 年，香港。马礼逊学堂校长塞缪尔·布朗决心做一件前无古人的事情。在教室里，因为健康问题，他与学生告别，准备回老家美国。这位传教士问眼前的数十位广东农家子弟：我非常热爱这所学校，希望带走几个学生，直至他们完成学业，有谁想跟我走？

谁愿意打破这片寂静呢？来自广东香山的农家子弟黄宽站了起来。

