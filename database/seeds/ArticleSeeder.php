在那个班级里，与黄宽一起站起来的只有容闳、黄胜。

若干年后，容闳将成为「中国留学生之父」；黄胜则是中文报业先驱，创办中国自办的第一家印刷企业；黄宽成为中国第一位留英博士，他短暂的一生翻译了 34 部医学著作，将大量的医学术语引入中文。

至少对黄宽来说，这样的命运，多少有些出乎意料。

