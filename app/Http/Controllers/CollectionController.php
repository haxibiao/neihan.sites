    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $collection        = Collection::with('user')->with('follows')->findOrFail($id);
        $data['new'] = $collection->hasManyArticles()->orderBy('id', 'desc')->paginate(10);
        $collection->count = count($data['new']);
        $data['commented'] = $collection->hasManyArticles()->orderBy('updated_at', 'desc')->paginate(10);
        $data['old']       = null;
        return view('collection.show')->withCollection($collection)->withData($data);
    }

